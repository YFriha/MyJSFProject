package entities;

import java.io.Serializable;

/**
 *
 * @author Lachgar
 */
public class Service implements Serializable{
    private String id;
    private String nom;

    public Service() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
}
